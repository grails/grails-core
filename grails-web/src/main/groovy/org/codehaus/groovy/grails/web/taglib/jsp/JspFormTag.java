package org.codehaus.groovy.grails.web.taglib.jsp;

/**
 * JSP facade onto the GSP form tag
 *
 * @author Graeme Rocher
 */
public class JspFormTag extends JspInvokeGrailsTagLibTag {
    private static final long serialVersionUID = -3045238592311090749L;

    private static final String TAG_NAME = "form";

    private String controller;
    private String action;
    private String id;
    private String url;
    private String params;
    private String method;

    public JspFormTag() {
        setTagName(TAG_NAME);
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public String getParams() {
        return params;
    }

    public void setParams(String params) {
        this.params = params;
    }

    public String getController() {
        return controller;
    }

    public void setController(String controller) {
        this.controller = controller;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
